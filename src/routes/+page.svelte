<h1>Welcome to a simple SvelteKit setup</h1>
<p>Visit <a href="https://kit.svelte.dev">kit.svelte.dev</a> to read the SvelteKit documentation</p>
<p>
	Content is written as markdown files and stored in <code>src/pages</code>.<br/>
	You can use Frontmatter too and easily connect to a CMS such as <a href="https://forestry.io/">Forestry</a>
	or <a href="https://www.netlifycms.org/">Netlify CMS</a>
</p>
<p>
	This page uses a basic CSS theme called <a href="https://markdowncss.github.io/splendor/">Splendor</a>. You can easily change the
	CSS by changing the imports in <code>+layout.svelte</code>
</p>
